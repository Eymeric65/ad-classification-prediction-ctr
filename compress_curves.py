"""Compress each ad's cumulative-CTR trajectory to a few PCA scores — and back.

A whole variable-length curve (cumu_ctr over delivery_days) becomes N_COMPONENTS
numbers. Predict those few numbers from creative features, then `decode` them to
rebuild the full curve.

Two files, cleanly separated:
  curve_config.yaml      SETTINGS  — your choices, shared across the toolchain.
  data/curve_codebook.yaml  RESULT — the fitted PCA codebook (grid + mean + components).

Pipeline:
  1. Resample every ad's curve onto a common grid (in log-CTR space).
  2. PCA the resampled matrix -> keep N_COMPONENTS scores per ad.

Usage:
  uv run python compress_curves.py fit       # fit PCA -> codebook + scores.parquet
  uv run python compress_curves.py encode    # curves -> scores.parquet (uses saved codebook)
  uv run python compress_curves.py decode <ad_id>   # print a reconstructed curve
"""
import sys
import numpy as np
import pandas as pd
import yaml

CONFIG_PATH = "curve_config.yaml"

# Settings that change what the codebook looks like — stamped into the codebook so
# encode/decode can warn if the config drifted since the last fit.
CODEBOOK_KEYS = ["n_components", "grid_points", "time_axis", "absolute_days_cap",
                 "log_transform", "columns"]


# ---- settings (read-only, never written by this tool) -----------------------

def load_settings(path=CONFIG_PATH):
    with open(path) as f:
        return yaml.safe_load(f)


# ---- codebook (a result; written by `fit`, read by encode/decode) -----------

def save_codebook(cb, cfg):
    cb = dict(cb, fit_settings={k: cfg[k] for k in CODEBOOK_KEYS})
    header = ("# Fitted PCA codebook — generated by `compress_curves.py fit`.\n"
              "# A result, not a setting. Do not hand-edit; re-run `fit` to regenerate.\n")
    with open(cfg["paths"]["codebook"], "w") as f:
        f.write(header)
        yaml.safe_dump(cb, f, sort_keys=False, default_flow_style=None, width=100)


def load_codebook(cfg):
    path = cfg["paths"]["codebook"]
    try:
        with open(path) as f:
            cb = yaml.safe_load(f)
    except FileNotFoundError:
        raise RuntimeError(f"No codebook at {path} — run `fit` first.")
    stamped = cb.get("fit_settings", {})
    current = {k: cfg[k] for k in CODEBOOK_KEYS}
    if stamped != current:
        diff = [k for k in current if stamped.get(k) != current[k]]
        print(f"  WARNING: {path} was fit under different settings (differ: {diff}). "
              f"Re-run `fit`.", file=sys.stderr)
    return cb


# ---- curve building ---------------------------------------------------------

def _grid(cfg):
    return np.linspace(0.0, 1.0, cfg["grid_points"])


def _resample_one(days, values, cfg, grid):
    """One ad's (days, values) -> fixed-length vector on the common grid."""
    order = np.argsort(days)
    days, values = days[order], values[order]
    y = np.log(values) if cfg["log_transform"] else values

    if cfg["time_axis"] == "absolute_days":
        cap = cfg["absolute_days_cap"]
        t = np.clip(days, 0, cap) / cap
    else:  # normalized_lifetime
        span = days.max() - days.min()
        t = (days - days.min()) / span if span > 0 else np.linspace(0, 1, len(days))

    return np.interp(grid, t, y)  # np.interp clamps to the curve's ends outside [t0,t1]


def build_matrix(laps, cfg):
    """laps DataFrame -> (X matrix (n_ads, grid_points), ad_ids array)."""
    grid = _grid(cfg)
    idc, tc, vc = cfg["columns"]["id"], cfg["columns"]["time"], cfg["columns"]["value"]
    laps = laps[[idc, tc, vc]].sort_values([idc, tc])
    rows, ids = [], []
    for ad_id, g in laps.groupby(idc, sort=True):
        rows.append(_resample_one(g[tc].to_numpy(float), g[vc].to_numpy(float), cfg, grid))
        ids.append(ad_id)
    return np.vstack(rows), np.array(ids)


# ---- fit / encode / decode --------------------------------------------------

def fit(laps, cfg):
    """Fit PCA on the resampled curves; return (codebook, ad_ids, scores)."""
    from sklearn.decomposition import PCA

    X, ad_ids = build_matrix(laps, cfg)
    pca = PCA(n_components=cfg["n_components"]).fit(X)
    cb = {
        "grid": _grid(cfg).tolist(),
        "mean": pca.mean_.tolist(),                       # length grid_points (log space)
        "components": pca.components_.tolist(),            # n_components x grid_points
        "explained_variance_ratio": pca.explained_variance_ratio_.tolist(),
    }
    scores = (X - pca.mean_) @ pca.components_.T           # (n_ads, n_components)
    return cb, ad_ids, scores


def encode(laps, cfg, cb=None):
    """Curves -> scores using the saved codebook. Returns (ad_ids, scores)."""
    cb = cb or load_codebook(cfg)
    X, ad_ids = build_matrix(laps, cfg)
    mean, comps = np.asarray(cb["mean"]), np.asarray(cb["components"])
    return ad_ids, (X - mean) @ comps.T


def decode(scores, cfg, cb=None):
    """Scores (n, n_components) -> reconstructed curves on the grid, in CTR space."""
    cb = cb or load_codebook(cfg)
    mean, comps = np.asarray(cb["mean"]), np.asarray(cb["components"])
    X = np.atleast_2d(scores) @ comps + mean
    return np.exp(X) if cfg["log_transform"] else X


def scores_frame(ad_ids, scores, cfg):
    cols = {f"pc{i}": scores[:, i] for i in range(cfg["n_components"])}
    return pd.DataFrame({cfg["columns"]["id"]: ad_ids, **cols})


# ---- CLI --------------------------------------------------------------------

def _cmd_fit():
    cfg = load_settings()
    laps = pd.read_parquet(cfg["paths"]["laps"])
    print(f"fitting PCA({cfg['n_components']}) on {laps[cfg['columns']['id']].nunique():,} "
          f"curves ({cfg['time_axis']}, log={cfg['log_transform']}) ...")
    cb, ad_ids, scores = fit(laps, cfg)
    save_codebook(cb, cfg)
    evr = np.cumsum(cb["explained_variance_ratio"])
    print(f"  cumulative explained variance: {evr[-1]*100:.3f}% (1 comp={evr[0]*100:.2f}%)")
    scores_frame(ad_ids, scores, cfg).to_parquet(cfg["paths"]["scores"], index=False)
    print(f"  wrote {cfg['paths']['codebook']} and {cfg['paths']['scores']} "
          f"({len(ad_ids):,} ads)")


def _cmd_encode():
    cfg = load_settings()
    laps = pd.read_parquet(cfg["paths"]["laps"])
    ad_ids, scores = encode(laps, cfg)
    scores_frame(ad_ids, scores, cfg).to_parquet(cfg["paths"]["scores"], index=False)
    print(f"wrote {cfg['paths']['scores']}: {len(ad_ids):,} ads x {cfg['n_components']} scores")


def _cmd_decode(ad_id):
    cfg = load_settings()
    s = pd.read_parquet(cfg["paths"]["scores"])
    idc = cfg["columns"]["id"]
    row = s[s[idc].astype(str) == str(ad_id)]
    if row.empty:
        raise SystemExit(f"ad_id {ad_id} not found in {cfg['paths']['scores']}")
    scores = row[[f"pc{i}" for i in range(cfg["n_components"])]].to_numpy()
    curve = decode(scores, cfg)[0]
    grid = _grid(cfg)
    print(f"reconstructed {cfg['columns']['value']} for {idc}={ad_id} (grid point -> value):")
    for t, c in zip(grid, curve):
        print(f"  t={t:4.2f}  value={c:.6f}")


if __name__ == "__main__":
    cmd = sys.argv[1] if len(sys.argv) > 1 else "fit"
    if cmd == "fit":
        _cmd_fit()
    elif cmd == "encode":
        _cmd_encode()
    elif cmd == "decode":
        _cmd_decode(sys.argv[2])
    else:
        raise SystemExit(f"unknown command {cmd!r}; use fit | encode | decode <ad_id>")
